export function geocodeAddresses(
  addressData: { address: string; count: number }[]
): GeoLocation[] {
  const results: GeoLocation[] = [];

  for (const item of addressData) {
    const addr = item.address.trim();
    if (!addr) continue;

    let coords = COORDINATES[addr];
    if (!coords) {
      for (const [key, val] of Object.entries(COORDINATES)) {
        if (addr.includes(key) || key.includes(addr)) {
          coords = val;
          break;
        }
      }
    }

    if (coords) {
      results.push({
        address: addr,
        lat: coords[0],
        lng: coords[1],
        count: item.count,
      });
    }
  }

  return results;
}
